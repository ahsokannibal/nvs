					<h2>Dépôt de thunes</h2>
					<p>Pour pouvoir déposer de la thune dans la banque de la compagnie, vous devez respecter plusieurs règles :</p>
					<ul>
						<li>Déposer au minimum 25 thunes (et donc en posséder au moins 25 sur vous)</li>
						<li>Avoir un délai de 8h entre votre dernier retrait et votre dépôt</li>
					</ul>
					<h2>Retrait de thunes</h2>
					<p>Pour pouvoir retirer de la thune de la banque de la compagnie, vous devez simplement posséder au minimum le montant que vous souhaitez retirer (il n'est pas possible de retirer un montant que vous ne possédez pas)</p>
					<h2>Emprunt de thunes</h2>
					<p>Lorsque vous avez besoin d'un certain montant de thunes et que vous ne possédez pas ce montant mais que la banque de la compagnie le possède, vous pouvez faire une demande d'emprunt à la banque.</p>
					<p>Cette demande d'emprunt arrivera sur la console du trésorier et du chef de la compagnie qui valideront ou refuseront la demande. Nous vous conseillons donc, afin de mettre toutes les chances de votre côté, de négocier cet emprunt avec ces personnes avant de faire votre demande.</p>
					<p>Si l'emprunt est refusé, vous recevrez un MP vous notifiant de ce refus.</p>
					<p>Si l'emprunt est validé, vous obtiendrez la thune sur votre compte perso dans la banque de la compagnie, vous pourrez alors le retirer quand vous en aurez besoin.</p>
					<p><b>Attention !</b> Un emprunt implique beaucoup de choses :</p>
					<ul>
						<li>Vous devez rembourser cet emprunt et donc tous vos futurs dépôts passerons automatiquement, et sans votre validation, au remboursement de cet emprunt</li>
						<li>Vous ne pouvez pas quitter la compagnie tant que vous n'aurez pas réglé votre dette ! (seul le chef de compagnie peut décider de virer votre perso en faisant fit de la dette)</li>
						<li>Si la dette de l'emprunt est sur un grouillot, il vous sera impossible de virer ce grouillot tant qu'il n'aura pas réglé sa dette</li>
					</ul>
					<h2>Virement de thunes à un autre perso de la compagnie</h2>
					<p>Vous avez la possibilité d'effectuer des virements vers un autre perso de la compagnie. Il n'existe pas de dette inscrite au niveau de la banque sur ces virements, à vous donc de vous arranger entre vous et de faire attention à ne pas virer votre thune sur le compte de n'importe qui.</p>
